num = 10

num += 5 # num = num + 5 = 10 + 5 = 15
print(f"num += 5 : {num}")

num -= 10 # num = num - 10 = 15 - 10 = 5
print(f"num -= 5 : {num}")

num *= 2 # num = num * 2 = 5 * 2 = 10
print(f"num *= 5 : {num}")

num /= 5 # num = num /5 = 10 / 5 = 2
print(f"num /= 5 : {num}")

num %= 2 # num = num % 2 = 2%2 = 0
print(f"num %= 2 : {num}")
